/**
 * Structured Shillong locality data — the geographic backbone for SCHEDULED
 * driver matching (service areas, locality affinity, proximity proxy, adjacency
 * fallback). The instant flow matches on live GPS; the scheduled flow has no live
 * GPS at booking time, so it matches on these localities instead.
 *
 * Each locality has:
 *   id           kebab-case stable id (store THIS on drivers/orders, never the label)
 *   name         display label (matches frontend/constants/localities.js)
 *   aliases      alternate spellings used to normalize free-text input
 *   zone         coarse cluster — drives the adjacency fallback (see ZONE_ADJACENCY)
 *   centroid     [lng, lat] — APPROXIMATE. See the banner below.
 *
 * ⚠️ CENTROIDS ARE HAND-PLACED APPROXIMATIONS, NOT SURVEYED COORDINATES.
 *    They are good enough for a coarse proximity *proxy* and for sanity, but the
 *    proximity scoring term should treat them as low-confidence. Run
 *    `npm run seed:localities` (backend/src/seeds/geocode-localities.js) to
 *    regenerate accurate centroids from Google Geocoding into
 *    `localities.centroids.json`, which this module overlays automatically.
 *    Until CENTROIDS_VERIFIED is true, the scorer down-weights / neutralizes the
 *    proximity-proxy term (see bestFit.js).
 *
 * Adjacency is modelled at the ZONE level (ZONE_ADJACENCY), not per-locality —
 * a maintainable, low-error approximation. `areAdjacent(a, b)` is true when two
 * localities share a zone or sit in neighbouring zones. This powers the
 * "widen to adjacent localities" fallback before escalating to a human.
 */

