// Build the canonical list, overlaying any verified centroids.
const LOCALITIES = RAW
  // de-dupe any accidental id collisions defensively (last wins)
  .filter((row, i, arr) => arr.findIndex((r) => r[0] === row[0]) === i)
  .map(([id, name, aliases, zone, centroid]) => ({
    id,
    name,
    aliases,
    zone,
    centroid: VERIFIED_CENTROIDS[id] || centroid, // [lng, lat]
  }));

// True only once every locality has a generated (surveyed) centroid.
const CENTROIDS_VERIFIED =
  LOCALITIES.length > 0 && LOCALITIES.every((l) => Array.isArray(VERIFIED_CENTROIDS[l.id]));

/* ------------------------------------------------------------------ */
/* Lookups                                                            */
/* ------------------------------------------------------------------ */

const _byId = new Map(LOCALITIES.map((l) => [l.id, l]));

// Normalize a free-text locality string -> canonical locality (or null).
const _normKey = (s) =>
  String(s || '')
    .toLowerCase()
    .replace(/\(.*?\)/g, ' ') // drop parentheticals e.g. "(Iewduh)"
    .replace(/[^a-z0-9]+/g, ' ')
    .trim();

const _byNorm = new Map();
for (const l of LOCALITIES) {
  _byNorm.set(_normKey(l.name), l);
  _byNorm.set(_normKey(l.id), l);
  for (const a of l.aliases) _byNorm.set(_normKey(a), l);
}

/** id -> locality object (or null). */
function byId(id) {
  return _byId.get(String(id)) || null;
}

/** Free-text "Laitumkhrah" / "bara bazaar" / id -> canonical locality (or null). */
function normalizeLocality(text) {
  if (!text) return null;
  return _byNorm.get(_normKey(text)) || null;
}

/** Locality id for an order (normalizes its free-text deliveryAddress.locality). */
function localityIdForText(text) {
  const l = normalizeLocality(text);
  return l ? l.id : null;
}

/**
 * Scan a free-text address for any known locality name/alias and return its id
 * (longest match wins). Recovers a locality from a reverse-geocoded line like
 * "Umpling, Shillong, Meghalaya 793006" when no explicit locality was provided.
 */
function detectFromText(text) {
  if (!text) return null;
  const hay = ` ${_normKey(text)} `;
  let best = null;
  for (const l of LOCALITIES) {
    for (const c of [l.name, ...l.aliases]) {
      const key = _normKey(c);
      if (key && hay.includes(` ${key} `) && (!best || key.length > best.len)) {
        best = { id: l.id, len: key.length };
      }
    }
  }
  return best ? best.id : null;
}

/** [lng, lat] centroid for a locality id (or null). */
function centroidOf(id) {
  const l = byId(id);
  return l ? l.centroid : null;
}

/** { lat, lng } centroid (geo.js convention) for a locality id (or null). */
function centerOf(id) {
  const c = centroidOf(id);
  return c ? { lng: c[0], lat: c[1] } : null;
}

/** True if two locality ids are in the same or neighbouring zones. */
function areAdjacent(idA, idB) {
  const a = byId(idA);
  const b = byId(idB);
  if (!a || !b) return false;
  if (a.zone === b.zone) return true;
  return (ZONE_ADJACENCY[a.zone] || []).includes(b.zone);
}

/** All locality ids adjacent to the given id (same zone + neighbouring zones), excluding itself. */
function adjacentIds(id) {
  const base = byId(id);
  if (!base) return [];
  const zones = new Set([base.zone, ...(ZONE_ADJACENCY[base.zone] || [])]);
  return LOCALITIES.filter((l) => l.id !== id && zones.has(l.zone)).map((l) => l.id);
}

